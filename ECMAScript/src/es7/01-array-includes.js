// El método includes determina si un array o string incluye un determinado elemento. Devuelve true o false, si existe o no respectivamente. Este método recibe dos argumentos: El elemento a comparar y el índice inicial desde donde comparar hasta el último elemento.

const frutas = ["manzana", "pera", "piña", "uva"]

frutas.includes("manzana") // true
frutas.includes("Pera") // false
